async function reloadFile(file) {
  file = file.url || file;
  let fileP = fileURLToPath(file);
  fs.watchFile(fileP, () => {
    fs.unwatchFile(fileP);
    console.log(`Update File "${fileP}"`);
    import(`${file}?update=${Date.now()}`);
  });
}

